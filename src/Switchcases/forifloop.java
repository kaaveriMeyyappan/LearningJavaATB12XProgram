package Switchcases;

public class forifloop {
    public static void main(String[] args) {
        for(int priya=0; priya < 18; priya++) {
            if(priya > 15){
                System.out.println("Gift from dad iphone");
            }
            else{
                System.out.println("No gift");
            }
        }
    }
}
