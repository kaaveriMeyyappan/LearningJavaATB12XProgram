package Task_001_Java_Multiples;

public class Arithmetic {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;
        float f = 3.14f;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a/f);
    }
}
