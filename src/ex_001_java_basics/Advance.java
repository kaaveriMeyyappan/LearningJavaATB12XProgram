package ex_001_java_basics;

public class Advance {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(a++ + a);
        int b = 10;
        System.out.println(b++ + ++b);
        System.out.println(b);
        int c = 5;
        System.out.println(++c + ++c);
    }
}
